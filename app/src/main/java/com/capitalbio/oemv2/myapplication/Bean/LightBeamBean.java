public class LightBeamBean {

    private int[] lt = new int[2];//光束的左上点
    private int[] rt = new int[2];//光束的右上点
    private int[] rb = new int[2];//光束的右下点
    private int[] lb = new int[2];//光束的左下点

    private int color;//光束的颜色

    private int alpha;//光束的透明度

    private int[] relationship = new int[2];//表示光束与舞台任务的关系，如，第一盏灯照在第一个人身上，第一个人照在第二个人身上等。

    public LightBeamBean() {
    }

    public int[] getLt() {
        return lt;
    }

    public void setLt(int[] lt) {
        this.lt = lt;
    }

    public int[] getRt() {
        return rt;
    }

    public void setRt(int[] rt) {
        this.rt = rt;
    }

    public int[] getRb() {
        return rb;
    }

    public void setRb(int[] rb) {
        this.rb = rb;
    }

    public int[] getLb() {
        return lb;
    }

    public void setLb(int[] lb) {
        this.lb = lb;
    }

    public int getColor() {
        return color;
    }

    public void setColor(int color) {
        this.color = color;
    }

    public int getAlpha() {
        return alpha;
    }

    public void setAlpha(int alpha) {
        this.alpha = alpha;
    }

    public int[] getRelationship() {
        return relationship;
    }

    public void setRelationship(int[] relationship) {
        this.relationship = relationship;
    }
}
