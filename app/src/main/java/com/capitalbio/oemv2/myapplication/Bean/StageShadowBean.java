public class StageShadowBean {

    private int l;//阴影的左
    private int t;//阴影的上
    private int r;//阴影的右
    private int b;//阴影的下

    private int num;//当前是第几个人的影子

    public StageShadowBean() {
    }

    public int getL() {
        return l;
    }

    public void setL(int l) {
        this.l = l;
    }

    public int getT() {
        return t;
    }

    public void setT(int t) {
        this.t = t;
    }

    public int getR() {
        return r;
    }

    public void setR(int r) {
        this.r = r;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }
}
