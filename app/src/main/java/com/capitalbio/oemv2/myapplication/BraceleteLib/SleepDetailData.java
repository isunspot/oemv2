package com.capitalbio.oemv2.myapplication.BraceleteLib;

import org.xutils.db.annotation.Column;
import org.xutils.db.annotation.Table;

/**
 * Created by chengkun on 16-3-1.
 */
@Table(name = "sleepDetailData")
public class SleepDetailData {

    //用户名
    @Column(name = "userName")
    private String userName;

    //id
    @Column(name = "id", isId = true)
    private int id;

    //睡眠类型
    @Column(name = "sleepType")
    private String sleepType;

    //long睡眠时间
    @Column(name = "longSleepTime")
    private long longSleepTime;

    //格式化时间 yyyy-mm-dd hh:MM:ss
    @Column(name = "fomatTime")
    private String fomatTime;

    //年月日
    @Column(name = "ymd")
    private String ymd;

    //小时
    @Column(name = "hour")
    private String hour;

    //是否上传
    @Column(name = "isUpload")
    private boolean isUpload;

    //数据源
    @Column(name = "dataSource")
    private String dataSource;

    //记录创建时间
    @Column(name = "ctime")
    private String ctime;

    public String getSleepType() {
        return sleepType;
    }

    public void setSleepType(String sleepType) {
        this.sleepType = sleepType;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public long getLongSleepTime() {
        return longSleepTime;
    }

    public void setLongSleepTime(long longSleepTime) {
        this.longSleepTime = longSleepTime;
    }

    public String getFomatTime() {
        return fomatTime;
    }

    public void setFomatTime(String fomatTime) {
        this.fomatTime = fomatTime;
    }


    public String getYmd() {
        return ymd;
    }

    public void setYmd(String ymd) {
        this.ymd = ymd;
    }

    public String getHour() {
        return hour;
    }

    public void setHour(String hour) {
        this.hour = hour;
    }

    public boolean isUpload() {
        return isUpload;
    }

    public void setIsUpload(boolean isUpload) {
        this.isUpload = isUpload;
    }

    public String getDataSource() {
        return dataSource;
    }

    public void setDataSource(String dataSource) {
        this.dataSource = dataSource;
    }

    public String getCtime() {
        return ctime;
    }

    public void setCtime(String ctime) {
        this.ctime = ctime;
    }
}
