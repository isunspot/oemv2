public class SleepDataTotalJson {
    private int sleepQuality;
    private String timeToSleep;//入睡时间 (单位为分钟)
    private int shallowSleepTime;//浅睡时间(单位为分钟)
    private int deepSleepTime;//深睡时间(单位为分钟)

    private  int awakeTime;//清醒时间(单位为分钟)
    private int wakeUpNumber;//唤醒次数
    private String sleepDate;//睡眠日期
    private int totalTime;//总时间

    public int getAwakeTime() {
        return awakeTime;
    }

    public void setAwakeTime(int awakeTime) {
        this.awakeTime = awakeTime;
    }

    public int getDeepSleepTime() {
        return deepSleepTime;
    }

    public void setDeepSleepTime(int deepSleepTime) {
        this.deepSleepTime = deepSleepTime;
    }

    public int getShallowSleepTime() {
        return shallowSleepTime;
    }

    public void setShallowSleepTime(int shallowSleepTime) {
        this.shallowSleepTime = shallowSleepTime;
    }

    public String getSleepDate() {
        return sleepDate;
    }

    public void setSleepDate(String sleepDate) {
        this.sleepDate = sleepDate;
    }

    public int getSleepQuality() {
        return sleepQuality;
    }

    public void setSleepQuality(int sleepQuality) {
        this.sleepQuality = sleepQuality;
    }

    public String getTimeToSleep() {
        return timeToSleep;
    }

    public void setTimeToSleep(String timeToSleep) {
        this.timeToSleep = timeToSleep;
    }

    public int getWakeUpNumber() {
        return wakeUpNumber;
    }

    public void setWakeUpNumber(int wakeUpNumber) {
        this.wakeUpNumber = wakeUpNumber;
    }

    public int getTotalTime() {
        return totalTime;
    }

    public void setTotalTime(int totalTime) {
        this.totalTime = totalTime;
    }

    @Override
    public String toString() {
        return "SleepDataTotalJson{" +
                "awakeTime=" + awakeTime +
                ", sleepQuality=" + sleepQuality +
                ", timeToSleep='" + timeToSleep + '\'' +
                ", shallowSleepTime=" + shallowSleepTime +
                ", deepSleepTime=" + deepSleepTime +
                ", wakeUpNumber=" + wakeUpNumber +
                ", sleepDate='" + sleepDate + '\'' +
                ", totalTime='" + totalTime + '\'' +
                '}';
    }
}
