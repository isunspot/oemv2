package com.capitalbio.oemv2.myapplication.FirstPeriod.Utils;

public class CityNameUtil {
	
	public static String transfer(String cityname){
		String s = "北京";
		//字符串的总长度
		int length = cityname.length();
		//去最后一个字符
		String lastchar = cityname.substring(length-1, length);
		//
		if(lastchar.equals("市")){
			//把最后一个市字去掉
			s = cityname.substring(0, length-1);
		}
		
		return s;
		
	}

}
