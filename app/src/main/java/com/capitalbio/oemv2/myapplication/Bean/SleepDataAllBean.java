public class SleepDataAllBean {
    //设备id
    private int deviceId;
    //睡眠质量
    private String sleepQuality;
    //入睡时间
    private int fallSleepHour;
    //清醒时间
    private int awakeHour;
    //浅睡时间
    private int lightSleepTime;
    //深睡时间
    private int deepSleepTime;
    //唤醒次数
    private int awakeTime;
    //总时间
    private int totalTime;
    //是否上传
    private boolean isUpload;
    //数据来源
    private String dataSource;

    public SleepDataAllBean() {
    }

    public int getDeviceId() {
        return deviceId;
    }

    public void setDeviceId(int deviceId) {
        this.deviceId = deviceId;
    }

    public String getSleepQuality() {
        return sleepQuality;
    }

    public void setSleepQuality(String sleepQuality) {
        this.sleepQuality = sleepQuality;
    }

    public int getFallSleepHour() {
        return fallSleepHour;
    }

    public void setFallSleepHour(int fallSleepHour) {
        this.fallSleepHour = fallSleepHour;
    }

    public int getAwakeHour() {
        return awakeHour;
    }

    public void setAwakeHour(int awakeHour) {
        this.awakeHour = awakeHour;
    }

    public int getLightSleepTime() {
        return lightSleepTime;
    }

    public void setLightSleepTime(int lightSleepTime) {
        this.lightSleepTime = lightSleepTime;
    }

    public int getDeepSleepTime() {
        return deepSleepTime;
    }

    public void setDeepSleepTime(int deepSleepTime) {
        this.deepSleepTime = deepSleepTime;
    }

    public int getAwakeTime() {
        return awakeTime;
    }

    public void setAwakeTime(int awakeTime) {
        this.awakeTime = awakeTime;
    }

    public int getTotalTime() {
        return totalTime;
    }

    public void setTotalTime(int totalTime) {
        this.totalTime = totalTime;
    }

    public boolean isUpload() {
        return isUpload;
    }

    public void setIsUpload(boolean isUpload) {
        this.isUpload = isUpload;
    }

    public String getDataSource() {
        return dataSource;
    }

    public void setDataSource(String dataSource) {
        this.dataSource = dataSource;
    }
}
