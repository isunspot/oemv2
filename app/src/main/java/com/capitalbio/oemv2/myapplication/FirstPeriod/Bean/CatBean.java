package com.capitalbio.oemv2.myapplication.FirstPeriod.Bean;


/**
 * 用来接收服务器端 ，返回的数据
 * @author jiantaotu
 *
 */

public class CatBean {
	
	private String pm2Point5;
	private String username;
	private String humidity;
	private String modelType;
	private String methanal;
	private String tvoc;
	private String pollutionlevel;
	private String testTime;
	private String deviceId;
	private String temperature;
	private String apikey;
	private String uploadTime;
	private String id;
	public CatBean() {
		super();
	}
	public String getPm2Point5() {
		return pm2Point5;
	}
	public void setPm2Point5(String pm2Point5) {
		this.pm2Point5 = pm2Point5;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getHumidity() {
		return humidity;
	}
	public void setHumidity(String humidity) {
		this.humidity = humidity;
	}
	public String getModelType() {
		return modelType;
	}
	public void setModelType(String modelType) {
		this.modelType = modelType;
	}
	public String getMethanal() {
		return methanal;
	}
	public void setMethanal(String methanal) {
		this.methanal = methanal;
	}
	public String getTvoc() {
		return tvoc;
	}
	public void setTvoc(String tvoc) {
		this.tvoc = tvoc;
	}
	public String getTestTime() {
		return testTime;
	}
	public void setTestTime(String testTime) {
		this.testTime = testTime;
	}
	public String getDeviceId() {
		return deviceId;
	}
	public void setDeviceId(String deviceId) {
		this.deviceId = deviceId;
	}
	public String getTemperature() {
		return temperature;
	}
	public void setTemperature(String temperature) {
		this.temperature = temperature;
	}
	public String getApikey() {
		return apikey;
	}
	public void setApikey(String apikey) {
		this.apikey = apikey;
	}
	public String getUploadTime() {
		return uploadTime;
	}
	public void setUploadTime(String uploadTime) {
		this.uploadTime = uploadTime;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}

	public String getPollutionlevel() {
		return pollutionlevel;
	}

	public void setPollutionlevel(String pollutionlevel) {
		this.pollutionlevel = pollutionlevel;
	}
}
